using System;

namespace Constants
{
    class Program
    {
        static void Main(string[] args)
        {
            /* Constants  - imutable values which are known at compile time and 
             *              do not change for the life of the program */
           const double pi = 3.14159;
            Console.WriteLine(pi);
            Console.Beep();
            Console.ReadKey();        
        }
    }
}
